DROP TABLE RESERVATION;
DROP TABLE TRAVELER;
DROP TABLE CRUISE;

--CREATE TABLE--

CREATE TABLE CRUISE(
	CID CHAR (2),
	PortName VARCHAR (20),
	CruiseLength NUMBER(2) CHECK(CruiseLength > 0),
	PRICE NUMBER(6, 2)DEFAULT 0 CHECK(PRICE >=0),
	CONSTRAINT PK_CRUISE PRIMARY KEY(CID)
	);
	
CREATE TABLE TRAVELER(
	TID CHAR (3),
	Tname VARCHAR (15),
	Tphone NUMBER (10),
	CONSTRAINT PK_TRAVELER PRIMARY KEY(TID)
	);
	
CREATE TABLE RESERVATION(
	CruiseID CHAR(2), 
	TravelerID CHAR(3),
	CrusieDate DATE,
	CONSTRAINT PK_RESERVATION PRIMARY KEY(CruiseID, TravelerID),
	FOREIGN KEY(CruiseID) REFERENCES CRUISE(CID),
	FOREIGN KEY(TravelerID) REFERENCES TRAVELER(TID)
	);
	
